const dictionaries: Record<SupportedLanguage, MessageMap> = {
  "zh-CN": zhCN,
  en,
};

export interface I18n {
  language: SupportedLanguage;
  preference: LanguagePreference;
  t: (key: string, replacements?: Replacements) => string;
}

export function resolveLanguage(
  preference: LanguagePreference | undefined = "auto",
  browserLanguage = getBrowserLanguage(),
): SupportedLanguage {
  if (preference === "zh-CN" || preference === "en") {
    return preference;
  }

  return browserLanguage.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function getI18n(preference: LanguagePreference | undefined = "auto"): I18n {
  const language = resolveLanguage(preference);
  return {
    language,
    preference: preference ?? "auto",
    t: (key, replacements) => translate(language, key, replacements),
  };
}

export function translate(
  language: SupportedLanguage,
  key: string,
  replacements: Replacements = {},
): string {
  const template = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_match, name: string) => {
    const value = replacements[name];
    return value === undefined ? `{${name}}` : String(value);
  });
}

export function applyElementTranslations(root: ParentNode, i18n: I18n): void {
  root.querySelectorAll<HTMLElement>("[data-i18n]").forEach((element) => {
    element.textContent = i18n.t(element.dataset.i18n ?? "");
  });

  root.querySelectorAll<HTMLInputElement | HTMLTextAreaElement>("[data-i18n-placeholder]").forEach((element) => {
    element.placeholder = i18n.t(element.dataset.i18nPlaceholder ?? "");
  });

  root.querySelectorAll<HTMLElement>("[data-i18n-title]").forEach((element) => {
    element.title = i18n.t(element.dataset.i18nTitle ?? "");
  });

  root.querySelectorAll<HTMLElement>("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", i18n.t(element.dataset.i18nAriaLabel ?? ""));
  });
}

function getBrowserLanguage(): string {
  try {
    return globalThis.navigator?.language || "en";
  } catch {
    return "en";
  }
}
